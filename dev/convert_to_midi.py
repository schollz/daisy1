NOTE_OFFSETS = {
    "C": 0,
    "C#": 1,
    "Db": 1,
    "D": 2,
    "D#": 3,
    "Eb": 3,
    "E": 4,
    "F": 5,
    "F#": 6,
    "Gb": 6,
    "G": 7,
    "G#": 8,
    "Ab": 8,
    "A": 9,
    "A#": 10,
    "Bb": 10,
    "B": 11,
}


def midi_note_to_number(note):
    # Extract the note letter and the octave number
    if len(note) == 3:
        note_name = note[:2]  # Handles sharp notes like C#, D#, etc.
        octave = int(note[2])
    else:
        note_name = note[0]  # Handles natural notes like C, D, E, etc.
        octave = int(note[1])

    # Calculate the MIDI number
    midi_number = 12 * (octave + 1) + NOTE_OFFSETS[note_name]

    return midi_number


